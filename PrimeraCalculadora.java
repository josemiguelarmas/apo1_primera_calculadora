

public class PrimeraCalculadora {
	
	public static void main(String[] args) {
		int numero1=8;
		int numero2=3;
		int suma = numero1+numero2; //Calcular suma
		int resta = numero1-numero2; //Calcular resta
		int producto = numero1*numero2; //Calcular multiplicacion
		int division = numero1/numero2 ; //Calcular division
		int modulo = numero1%numero2 ; //Calcular modulo
		System.out.println("La suma entre 8 y 3 da "+suma); //Mostrar suma
		System.out.println("La resta entre 8 y 3 da "+resta); // Mostrar resta
		System.out.println("La multiplicacion entre 8 y 3 da "+producto); //Mostrar producto
		System.out.println("La division entre 8 y 3 da "+division); //Mostrar division
		System.out.println("El residuo de la division entre 8 y 3 da "+modulo);	//Mostrar modulo
	}
}